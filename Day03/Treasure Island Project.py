print("Your mission is to find the treasure.")

path = input('What path? Press l for left or press r for right: ')
if path != 'l':
    print('Fall into a hole.\nGame Over.')

else:
    motion = input('What are you going to do? Press s for swim or press w for wait: ')
    if motion != 'w':
        print('Attacked by trout.\nGame Over.')

    else:
        door_color = input('What door do you chose? Press r for Red, b for Blue, y for Yellow: ')

        if door_color == 'r':
            print('Burned by fire.\nGame Over.')
        elif door_color == 'b':
            print('Eaten by beasts.\nGame Over.')
        elif door_color == 'y':
            print('You Win!')
        else:
            print('Game Over.')
